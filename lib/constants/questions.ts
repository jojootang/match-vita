// Utility functions
export function calculateCategoryScores(answers: Record<string, number>) {
  const categoryScores: Record<string, { total: number; count: number; average: number }> = {};

  Object.entries(answers).forEach(([questionId, score]) => {
    const question = QUESTIONS.find(q => q.id === questionId);
    if (question) {
      if (!categoryScores[question.category]) {
        categoryScores[question.category] = { total: 0, count: 0, average: 0 };
      }
      categoryScores[question.category].total += score;
      categoryScores[question.category].count += 1;
    }
  });

  // Calculate averages
  Object.keys(categoryScores).forEach(category => {
    const data = categoryScores[category];
    data.average = data.total / data.count;
  });

  return categoryScores;
}

export function determineHealthLevel(averageScore: number): 1 | 2 | 3 | 4 {
  if (averageScore <= 1.5) return 1; // Excellent
  if (averageScore <= 2.5) return 2; // Good
  if (averageScore <= 3.5) return 3; // Fair
  return 4; // Needs attention
}

